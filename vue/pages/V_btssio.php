<div class="container">
    <h3 class="text-center"><br>BTS Services Informatiques aux Organisations</h3>

    <div class="white">
    <br><br>
    <p><i class="bi bi-aspect-ratio"></i> Introduction : Le nouveau BTS SIO (Services Informatiques aux Organisations)
    , créé à la rentrée 2011, remplace le BTS Informatique de Gestion. Il forme les étudiants à la mise en place de 
    solutions informatiques au sein des entreprises et comprend 2 options : 
    la spécialité « solutions d’infrastructure, systèmes et réseaux » et la spécialité « solutions logicielles et 
    applications métiers ».</p>
    <br>
    <p><i class="bi bi-award"></i> Objectifs : Le diplômé du BTS SIO (Services informatiques aux organisations) 
    est formé à la mise en place de services informatiques en tant que salarié au sein des organisations 
    (entreprises, administrations, pme, pmi, etc...) soit en tant que consultant d'une SSII 
    (société de services d'ingénierie informatique), d'une société éditrice de logiciels ou d'une société de 
    conseils. Les services informatiques dont il aura la responsabilité concernent à la fois les solutions 
    techniques d'infrastructures, les applications logicielles, la maintenance et l'évolution de ces solutions dans 
    le respect des contraintes légales et stratégiques - en s'appuyant sur des normes de sécurité ou des guides de 
    bonnes pratiques. Le BTS SIO (BTS Services informatiques aux organisations) prépare à 2 métiers distincts, 
    représentés par les 2 spécialités du diplôme : Solutions d’infrastructure, systèmes et réseaux et solutions 
    logicielles et applications métiers.</p>
